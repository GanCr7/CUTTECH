def get_downloads_folder():
    return os.path.join(os.path.expanduser("~"), "Downloads")

def sanitize_layer_name(name):
    # Replace spaces with underscores, remove invalid characters
    return "".join(c if c.isalnum() or c in "_-" else "_" for c in name.replace(" ", "_"))

def write_ltg_valid(raw_data, output_filename):
    layers = []
    current_layer = None
    current_polyline = []

    for line in raw_data.splitlines():
        line = line.strip()
        if not line:
            continue

        parts = line.split()
        is_numeric = False
        if len(parts) >= 2:
            try:
                float(parts[0])
                float(parts[1])
                is_numeric = True
            except ValueError:
                pass

        if is_numeric:
            current_polyline.append((float(parts[0]), float(parts[1])))
        else:
            if current_layer and current_polyline:
                layers.append((current_layer, current_polyline))
            current_layer = sanitize_layer_name(line)
            current_polyline = []

    # add last layer if it has points
    if current_layer and current_polyline:
        layers.append((current_layer, current_polyline))

    output_path = os.path.join(get_downloads_folder(), output_filename)
    with open(output_path, "w", encoding="iso-8859-1", newline="\n") as f:
        f.write('<?xml version="1.0" encoding="iso-8859-1"?>\n')
        f.write('<!DOCTYPE ltg SYSTEM "Virtek LTG 1.0">\n')
        f.write('<ltg units="mm" quantity="1">\n')

        for layer_name, points in layers:
            f.write(f'  <layer name="{layer_name}">\n')
            f.write('    <polyline2d>\n')
            for x, y in points:
                f.write(f'      <point2d x="{x:.6f}" y="{y:.6f}" />\n')
            f.write('    </polyline2d>\n')
            f.write('  </layer>\n')

        f.write('</ltg>\n')

    print(f"â LTG file saved to Downloads: {output_path}")

# Run
